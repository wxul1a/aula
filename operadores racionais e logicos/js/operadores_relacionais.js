let valor1 = Math.floor(Math.random() * 5);
let valor2 = Math.floor(Math.random() * 5);

//Igualdade
let Igualdade = valor1 == valor2
console.log(`igualdade: ${igualdade}`);

//Diferença
let diferença = valor1 != valor2
console.log(`diferença: ${diferença}`);

//Maior que
let maiorQue = valor1 > valor2
console.log(`maior que: ${maiorQue}`);

//Menor que
let menorQue = valor1 <valor2
console.log(`Menor que: ${menorQue}`);

//Maior ou igual à
let maiorOuIgual = valor1 >= valor2
console.log(`Maior ou igual à: ${maiorOuIgual}`);

//Menor ou igual à
let menorOuIgual = valor1 <= valor2
console.log(`Menor ou igual à: ${menorOuIgual}`);